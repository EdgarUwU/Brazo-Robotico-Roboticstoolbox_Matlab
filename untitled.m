function pushbutton1_Callback(hObject, eventdata, handles)
t1=str2double(handles.edit1.String)*pi/180;%Obtenemos valor de la posicion 1
t2=str2double(handles.edit2.String)*pi/180;%Obtenemos valor de la posicion 2
t3=str2double(handles.edit3.String)*pi/180;%Obtenemos valor de la posicion 3
L_1=20;
L_2=50;
L_3=40;
L(1)=Link([0 L_1 0 pi/2]);%Se contruye el robot, en este caso yo utilize uno de 3 articulaciones
L(2)=Link([0 0 L_2 0]);   %aunque solo este usando 2, esto para obtener el valor del eje z
L(3)=Link([0 0 L_3 0]);   %%%%%%
Robot=SerialLink(L);      %Creamos el robot en base a los valores anteriores (L)
Robot.name='Proyectoo';   %Asignamos un nombre el cual se mostrara en el plot del robot
Robot.plot([t1 t2 t3]);   %Mostramos el plot del robot con los valores que obtuvimos de los edittext
T=Robot.fkine([t1 t2 t3]);%Hacemos el fkine para obtener las cordenadas de las articulaciones del robot, el arreglo
t= print(T);              %Creamos una nueva variable para poder imprimir los valores xyz en los edit
t1= strsplit(t,' ');      %Dividimos la linea de valores para solo utilizar los que nosotros deseamos
t2 =erase(t1,'(');        %Eliminamos valores dentro de los elemoentos divididos
t3= erase(t2,')');        %%%%
t4=erase(t3,',');         %%%
t4                        %Imprimimos t4 la cual este sera el arreglo del cual sacaremos los valores xyz
x1=t4(1,3);               %Obtenemos el valor de xyz de acuerdo a la posicion del arreglo t4
y1=t4(1,4);               %%%%%%
z1=t4(1,5);               %%%%%%%
v=str2double(get(handles.edit1,'String'));                                                     %Obtenemos posici√≥n del slider
writePosition(handles.servo,v/180);                                         %Escribimos la posici√≥n del slider en el servo
pos=readPosition(handles.servo)*180
v2=str2double(get(handles.edit3,'String'));                                                     %Obtenemos posici√≥n del slider
writePosition(handles.servo2,v2/180);                                         %Escribimos la posici√≥n del slider en el servo
pos2=readPosition(handles.servo2)*180
set(handles.edit4,'String',x1); %Imprimimos los valores en la gui
set(handles.edit5,'String',y1); %%%%%%
set(handles.edit6,'String',z1); %%%%%%%%%
guidata(hObject,handles);
